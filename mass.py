# mass converter functions

#kilograms to pounds
def kg2lb(num):
    return num*2.2

#pounds to kilograms
def lb2kg(num):
    return num/2.2

#slugs to kilograms
def slug2kg(num):
    return num*14.5939

#kilograms to slugs
def kg2slug(num):
    return num/14.5939

#slugs to pounds
def slug2lb(num):
    return num*(14.5939/2.2)

#pounds to slugs
def lb2slug(num):
    return num/(14.5939/2.2)
